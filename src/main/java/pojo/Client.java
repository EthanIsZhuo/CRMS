package pojo;

public class Client {
	int cid;
	String cSnum;
	String cName;
	int cType;
	String cPhone;
	String cDob;
	String cDno;
	public int getCid() {
		return cid;
	}
	public void setCid(int cid) {
		this.cid = cid;
	}
	public String getcSnum() {
		return cSnum;
	}
	public void setcSnum(String cSnum) {
		this.cSnum = cSnum;
	}
	public String getcName() {
		return cName;
	}
	public void setcName(String cName) {
		this.cName = cName;
	}
	public int getcType() {
		return cType;
	}
	public void setcType(int cType) {
		this.cType = cType;
	}
	public String getcPhone() {
		return cPhone;
	}
	public void setcPhone(String cPhone) {
		this.cPhone = cPhone;
	}
	public String getcDob() {
		return cDob;
	}
	public void setcDob(String cDob) {
		this.cDob = cDob;
	}
	public String getcDno() {
		return cDno;
	}
	public void setcDno(String cDno) {
		this.cDno = cDno;
	}
	
	
}
